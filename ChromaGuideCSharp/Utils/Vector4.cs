namespace ChromaSDK
{
    public struct Vector4
    {
        public float w;
        public float x;
        public float y;
        public float z;
        public Vector4(float x1, float y1, float z1, float w1)
        {
            x = x1;
            y = y1;
            z = z1;
            w = w1;
        }
    }
}
